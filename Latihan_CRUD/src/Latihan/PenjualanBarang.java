    private void kodeKonsumenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kodeKonsumenActionPerformed
        String kodeks,level;
        kodeks = String.valueOf(kodeKonsumen.getSelectedItem());
        
        if(kodeks == "KS 001"){
            level = "Platinum";
        }
        else if(kodeks == "KS 002"){
            level = "Gold";
        }
        else if(kodeks == "KS 003"){
            level = "Silver";
        }
        else{
            level = "Bronze";
        }  
        keterangan.setText(level);
    }//GEN-LAST:event_kodeKonsumenActionPerformed

    private void kodeBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kodeBarangActionPerformed
        String kdbarang,namabarang,per;
        double hargabarang;
        
        kdbarang = String.valueOf(kodeBarang.getSelectedItem());
        
        if(kdbarang == "KB 001"){
            namabarang = "Minyak";
            per = "Liter";
            hargabarang = 15000;
        }
        else if(kdbarang == "KB 002"){
            namabarang = "Gula";
            per = "Gram";
            hargabarang = 12000;
        }
        else if(kdbarang == "KB 003"){
            namabarang = "Telur";
            per = "Kilogram";
            hargabarang = 25000;
        }
        else{
            namabarang = "Beras";
            per = "Kilogram";
            hargabarang = 20000;
        }
        namaBarang.setText(namabarang);
        hargaBarang.setText(hargabarang+"");
        satuan.setText(per);
    }//GEN-LAST:event_kodeBarangActionPerformed

