    private void btnCekDiskonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekDiskonActionPerformed
        double hargabarang,diskon;
        int jumlahbeli;
        
        hargabarang = Double.parseDouble(hargaBarang.getText());
        jumlahbeli = Integer.parseInt(jumlahBeli.getText());
        
        if(jumlahbeli > 5){
            diskon = hargabarang*10/100;
        }
        else{
            diskon = 0;
        }
        this.diskon.setText(diskon+"");
    }//GEN-LAST:event_btnCekDiskonActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        double hargabarang,diskon,totalbayar;
        int jumlahbeli;
        
        hargabarang = Double.parseDouble(hargaBarang.getText());
        diskon = Double.parseDouble(this.diskon.getText());
        jumlahbeli = Integer.parseInt(jumlahBeli.getText());
        
        totalbayar = hargabarang * jumlahbeli - diskon;
        totalHarga.setText(totalbayar+"");
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        double totalbayar,uangbayar,uangkembalian;
        
        totalbayar = Double.parseDouble(totalHarga.getText());
        uangbayar = Double.parseDouble(bayar.getText());
        
       uangkembalian = uangbayar - totalbayar;
       kembalian.setText(uangkembalian+"");
    }//GEN-LAST:event_btnBayarActionPerformed

