    private void Time() {
        time = new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date date = new Date();
                jam = sdf.format(date);
                tanggal = sdf2.format(date);
            }
        });
        time.start();
    }

    private void tampilkan() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/petshop", "root", "");
            Statement st = conn.createStatement();
            String sql = "SELECT * FROM datapelanggan";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] obj = new Object[7];
                obj[0] = rs.getString("tanggal");
                obj[1] = rs.getString("jam");
                obj[2] = rs.getString("nama_pemilik");
                obj[3] = rs.getString("nama_kucing");
                obj[4] = rs.getString("jenis_kelamin");
                obj[5] = rs.getString("jenis_mandi");
                obj[6] = rs.getString("harga");
                model.addRow(obj);
            }
        } catch (Exception e) {
            System.out.println("Terjadi Kesalahan");
        }
    }

    private void hargaMandi() {
        String mandi = String.valueOf(jenisMandi.getSelectedItem());

        if (mandi == "Mandi Biasa") {
            harga = 50000;
        } else if (mandi == "Mandi Kutu") {
            harga = 60000;
        } else if (mandi == "Mandi Lengkap") {
            harga = 70000;
        } else {
            harga = 90000;
        }
        totalHarga = "Rp. " + df.format(harga);
    }

    private void reset() {
        namaPemilik.setText("");
        namaKucing.setText("");
        jenisKelamin.setSelectedItem("Jantan");
        jenisMandi.setSelectedItem("Mandi Biasa");
    }

