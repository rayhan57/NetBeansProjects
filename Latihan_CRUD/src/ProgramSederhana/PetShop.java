    private void btnOkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkeActionPerformed
        hargaMandi();
        String namapemilik = namaPemilik.getText();
        String namakucing = namaKucing.getText();
        String jeniskelamin = String.valueOf(jenisKelamin.getSelectedItem());
        String jenismandi = String.valueOf(jenisMandi.getSelectedItem());

        try {
            if (namapemilik.equals("") || namakucing.equals("")) {
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
            } else {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/petshop", "root", "");
                Statement st = conn.createStatement();
                String sql = "INSERT INTO datapelanggan VALUES" + "('" + tanggal + "','" + jam + "','" + namapemilik + "','" + namakucing + "','" + jeniskelamin + "','" + jenismandi + "', '" + totalHarga + "')";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Anda Berhasil Ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                tampilkan();
                reset();
            }
        } catch (Exception e) {
            System.out.println("Terjadi Kesalahan");
        }
    }//GEN-LAST:event_btnOkeActionPerformed

