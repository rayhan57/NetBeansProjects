    private void loadingProgress() {
        t = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= 570; i++) {
                    try {
                        loadingProgress.setPreferredSize(new Dimension(i, 7));
                        spam.setText(i + "");
                        spam.hide();
                        Thread.sleep(8);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
            }
        });
        t.start();
    }

    private void persentaseLoading() {
        t = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i <= 100; i++) {
                    try {
                        persentaseLoading.setText("Loading... " + i + "%");
                        Thread.sleep(50);
                        if (i == 100) {
                            Thread.sleep(1000);
                            dispose();
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
            }
        });
        t.start();
    }

