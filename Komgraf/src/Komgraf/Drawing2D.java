    public void paint(Graphics g) {
        g.drawLine(30, 50, 30, 250);

        Graphics2D g2 = (Graphics2D) g;
        BasicStroke garisTebal = new BasicStroke(3.0f);//Tebal garis

        g2.setStroke(garisTebal);
        g2.drawLine(100, 50, 100, 250);

        float[] garisPutus = new float[2];
        garisPutus[0] = 50.0f;//Panjang garis
        garisPutus[1] = 5.0f;//Panjang garis kosong

        BasicStroke garisPutusStandar = new BasicStroke(5.0f, BasicStroke.CAP_ROUND, BasicStroke.JOIN_BEVEL, 2.0f, garisPutus, 0.0f);
        g2.setStroke(garisPutusStandar);
        g2.drawLine(170, 50, 170, 250);
    }

